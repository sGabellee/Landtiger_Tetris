#include "LPC17xx.h"
#include "joystick.h"

/*----------------------------------------------------------------------------
  Function that initializes joysticks and switch them off
 *----------------------------------------------------------------------------*/

void joystick_init(void) {
    LPC_PINCON->PINSEL3 &= ~( (3<<18) | (3<<20) | (3<<22) | (3<<24) | (3<<26) );
    LPC_GPIO1->FIODIR   &= ~( (1<<25) | (1<<26) | (1<<27) | (1<<28) | (1<<29) );
}
